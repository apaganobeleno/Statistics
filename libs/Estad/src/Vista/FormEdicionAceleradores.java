    private void txtAceleradorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAceleradorKeyPressed
        // TODO add your handling code here:
        String modificadores = evt.getKeyModifiersText(evt.getModifiers());
        String tecla = evt.getKeyText(evt.getKeyCode());
        if (modificadores.length() == 0){
            txtAcelerador.setText(tecla);
        }else if (tecla.equalsIgnoreCase(modificadores)){
            txtAcelerador.setText(modificadores);
        }else{
            txtAcelerador.setText(modificadores+"+"+tecla);
        }
        
    }//GEN-LAST:event_txtAceleradorKeyPressed

