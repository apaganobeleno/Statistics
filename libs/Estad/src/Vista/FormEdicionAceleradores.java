    private void comAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comAceptarActionPerformed
        // TODO add your handling code here:
        try{
            String id = acelerador.getId();
            if(txtAcelerador.getText().length()>0 && txtCodigo.getText().length()>0){
            opAcel.ActualizaAcelerador(id, txtCodigo.getText(), txtAcelerador.getText(), txtDescripcion.getText());
            this.setVisible(false);
            borrar();    
            }else{
                JOptionPane.showMessageDialog(null, "El codigo y el acelerador son indispensables para la Actualizacion");
            }
            
        }catch(Exception e){
            if(txtAcelerador.getText().length()>0 && txtCodigo.getText().length()>0){
            opAcel.insertarAcelerador(txtCodigo.getText(), txtAcelerador.getText(), txtDescripcion.getText());
            this.setVisible(false);
            borrar();    
            }else{
                JOptionPane.showMessageDialog(null, "El codigo y el acelerador son indispensables para la creacion");
            }
            
        }
    }//GEN-LAST:event_comAceptarActionPerformed

