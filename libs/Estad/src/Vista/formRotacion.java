    private void comGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comGuardarActionPerformed
        // TODO add your handling code here:
        ope = new OperacionesRotacion(FormPrincipal.getCon(),FormPrincipal.getOperacionesBasicas(),this.juego.getId(),this.juego.getCodPropio(),this.getJuego().getCodOpp());
        int eval = ope.RevisarAlineacion(tblRotacion);
        if(eval == 0){
            
        }else if(eval == 1){
            JOptionPane.showMessageDialog(null, "La Rotacion contiene numeros repetidos en alguno de los 2 equipos,No se puede procesar de esta manera");
        }else if(eval == 2){
            int sel = JOptionPane.showConfirmDialog(null, "Algunos de los jugadores en la rotacion no pertencen al equipo\nÂ¿Desea continuar?","Alerta",JOptionPane.YES_NO_OPTION); 
            if(sel == JOptionPane.YES_OPTION){
                ope.guardarRotaciones(this.tblRotacion, String.valueOf(ope.getIdParcialActual()), this.juego.getCodPropio(), this.getJuego().getCodOpp());
                this.setVisible(false);
            }
        }else if(eval == 3){
            JOptionPane.showMessageDialog(null, "El numero del pasador debe estar dentro de la Rotacion definida");
        }else if(eval == 5){
            JOptionPane.showMessageDialog(null, "La rotacion contiene errores, revise que el pasador se encuentre en la rotacion");
        }else{
            ope.guardarRotaciones(this.tblRotacion, String.valueOf(ope.getIdParcialActual()), this.juego.getCodPropio(), this.getJuego().getCodOpp());
            this.setVisible(false);
        }
    }//GEN-LAST:event_comGuardarActionPerformed


